a = True # Membuat variabel a bernilai True untuk menjalankan while
i = 0  # Membuat variabel i bernilai 0 untuk menentukan bahwa program akan dimulai dari 0
while a:  # Perulangan while
    if i <= 4:  # Menentukan jumlah percabangan dengan kondisi i<=4 berarti dimulai dari 0-4 
        i += 1  # Menjumlahkan percabangan i<=4 (0+1, 1+1)
        print(f'Menjalankan Program {i} kali') # Penugasan 
    else: # Membuat else
        a = False # Agar program berhenti sesuai i<=4

        
